#include "../fdf.h"

int		clean_board(t_data *data)
{
	int x;
	int y;

	x = 2;
	y = 2;
	while(y < 1278)
	{
		while (x < 718)
		{
			mlx_pixel_put(data->mlx, data->win, y, x, 0X00000000);
			x++;
		}
		x = 2;
		y++;
	}
	return (0);
}
